document.addEventListener('DOMContentLoaded', () => {
    const allElementsToTranslate = document.querySelectorAll('[data-translate]');

    const langSwitcher = document.getElementById('customLangSwitcher');
    const selectedLangDisplay = langSwitcher.querySelector('.selected-lang-display');
    const langOptionsList = langSwitcher.querySelector('.lang-options-list');
    const selectedLangFlag = document.getElementById('selectedLangFlag');
    const selectedLangText = document.getElementById('selectedLangText');

    const setLanguage = (lang) => {
        allElementsToTranslate.forEach(element => {
            const key = element.getAttribute('data-translate');
            if (translations[lang] && translations[lang][key]) {
                if (element.querySelector('.highlight-skill') || ['projectsIntro', 'heroSubtitle', 'heroTechStack', 'aboutP2'].includes(key)) {
                    element.innerHTML = translations[lang][key];
                } else {
                    element.textContent = translations[lang][key];
                }
            } else if (translations.en[key]) {
                if (element.querySelector('.highlight-skill') || ['projectsIntro', 'heroSubtitle', 'heroTechStack', 'aboutP2'].includes(key)) {
                    element.innerHTML = translations.en[key];
                } else {
                    element.textContent = translations.en[key];
                }
            }
        });

        const toggleButton = document.getElementById('toggleMoreProjects');
        if (toggleButton) {
            const icon = toggleButton.querySelector('i').outerHTML;
            const projectsVisible = document.getElementById('more-projects-grid').style.display === 'grid';
            const currentTranslations = translations[lang] || translations.en;
            if (projectsVisible) {
                toggleButton.innerHTML = icon + ' ' + currentTranslations.btnShowFewerProjects;
            } else {
                toggleButton.innerHTML = icon + ' ' + currentTranslations.btnShowMoreProjects;
            }
        }

        document.documentElement.lang = lang;
        localStorage.setItem('selectedLang', lang);

        const selectedOption = langOptionsList.querySelector(`li[data-value="${lang}"]`);
        if (selectedOption) {
            selectedLangFlag.src = selectedOption.dataset.flagSrc;
            selectedLangFlag.alt = selectedOption.querySelector('img').alt;
            selectedLangText.textContent = selectedOption.dataset.text;
        }
        closeLangDropdown();
    };

    function toggleLangDropdown() {
        const isOpen = langSwitcher.classList.toggle('open');
        langOptionsList.style.display = isOpen ? 'block' : 'none';
        selectedLangDisplay.setAttribute('aria-expanded', isOpen.toString());
    }

    function closeLangDropdown() {
        langSwitcher.classList.remove('open');
        langOptionsList.style.display = 'none';
        selectedLangDisplay.setAttribute('aria-expanded', 'false');
    }

    // --- Set Dynamic Copyright Year ---
    const yearSpan = document.getElementById('currentYear');
    if (yearSpan) {
        yearSpan.textContent = new Date().getFullYear();
    }
    // --- End Set Dynamic Copyright Year ---
    selectedLangDisplay.addEventListener('click', (event) => {
        event.stopPropagation();
        toggleLangDropdown();
    });

    selectedLangDisplay.addEventListener('keydown', (event) => {
        if (event.key === 'Enter' || event.key === ' ') {
            event.preventDefault();
            toggleLangDropdown();
        } else if (event.key === "Escape" && langSwitcher.classList.contains('open')) {
            closeLangDropdown();
            selectedLangDisplay.focus();
        }
    });

    langOptionsList.querySelectorAll('li').forEach(option => {
        option.addEventListener('click', () => {
            setLanguage(option.dataset.value);
        });
        option.addEventListener('keydown', (event) => {
            if (event.key === 'Enter' || event.key === ' ') {
                event.preventDefault();
                setLanguage(option.dataset.value);
                selectedLangDisplay.focus();
            } else if (event.key === "Escape") {
                closeLangDropdown();
                selectedLangDisplay.focus();
            }
        });
    });

    document.addEventListener('click', (event) => {
        if (!langSwitcher.contains(event.target) && langSwitcher.classList.contains('open')) {
            closeLangDropdown();
        }
    });

    const preferredLang = localStorage.getItem('selectedLang') || 'en';
    setLanguage(preferredLang);

    const mobileMenuButton = document.getElementById('mobile-menu');
    const navMenu = document.getElementById('nav-menu');
    const navIcon = mobileMenuButton.querySelector('i');

    mobileMenuButton.addEventListener('click', () => {
        navMenu.classList.toggle('active');
        navIcon.classList.toggle('fa-bars');
        navIcon.classList.toggle('fa-times');
        if (langSwitcher.classList.contains('open')) {
            closeLangDropdown();
        }
    });

    const navLinks = document.querySelectorAll('#nav-menu ul .nav-link');
    navLinks.forEach(link => {
        link.addEventListener('click', () => {
            if (navMenu.classList.contains('active')) {
                navMenu.classList.remove('active');
                navIcon.classList.add('fa-bars');
                navIcon.classList.remove('fa-times');
            }
        });
    });

    const sections = document.querySelectorAll('section[id]');
    const headerNavLinks = document.querySelectorAll('header nav ul li a');
    const headerHeight = document.querySelector('header').offsetHeight;

    function navHighlighter() {
        let scrollY = window.pageYOffset;
        let currentSectionId = '';
        sections.forEach(current => {
            const sectionHeight = current.offsetHeight;
            const sectionTop = current.offsetTop - headerHeight - 70; // Increased offset slightly
            if (scrollY >= sectionTop && scrollY < sectionTop + sectionHeight) {
                currentSectionId = current.getAttribute('id');
            }
        });
        if ((window.innerHeight + scrollY) >= document.body.offsetHeight - 50) {
            currentSectionId = 'contact';
        }
        headerNavLinks.forEach(link => {
            link.classList.remove('active');
            if (link.getAttribute('href') === '#' + currentSectionId) {
                link.classList.add('active');
            }
        });
        if (!currentSectionId && scrollY < (sections[0] ? sections[0].offsetTop - headerHeight - 20 : 0)) {
            headerNavLinks.forEach(link => link.classList.remove('active'));
        }
    }
    window.addEventListener('scroll', navHighlighter);
    navHighlighter();

    const certificateModal = document.getElementById("certificateModal");
    const modalImg = document.getElementById("modalImage");
    const captionText = document.getElementById("caption");
    const closeBtn = document.querySelector(".close-modal-btn");
    document.querySelectorAll('.certificate-image-clickable').forEach(img => {
        img.onclick = function () {
            certificateModal.style.display = "block";
            modalImg.src = this.dataset.modalSrc || this.src;
            captionText.innerHTML = this.alt;
        }
    });
    if (closeBtn) closeBtn.onclick = () => certificateModal.style.display = "none";
    certificateModal.onclick = (event) => { if (event.target == certificateModal) certificateModal.style.display = "none"; }
    modalImg.onclick = (event) => event.stopPropagation();

    const slides = document.querySelectorAll('.fiverr-review-slide');
    const prevBtn = document.getElementById('prevReviewBtn');
    const nextBtn = document.getElementById('nextReviewBtn');
    let currentSlide = 0;
    function showSlide(index) {
        slides.forEach((slide, i) => slide.classList.toggle('active-slide', i === index));
        if (prevBtn) prevBtn.disabled = index === 0;
        if (nextBtn) nextBtn.disabled = index === slides.length - 1;
    }
    if (slides.length > 0) {
        showSlide(currentSlide);
        if (prevBtn) prevBtn.addEventListener('click', () => { if (currentSlide > 0) showSlide(--currentSlide); });
        if (nextBtn) nextBtn.addEventListener('click', () => { if (currentSlide < slides.length - 1) showSlide(++currentSlide); });
    } else {
        if (prevBtn && nextBtn) { prevBtn.style.display = 'none'; nextBtn.style.display = 'none'; }
    }

    const toggleButton = document.getElementById('toggleMoreProjects');
    const moreProjectsGrid = document.getElementById('more-projects-grid');
    let projectsVisible = false;
    if (toggleButton && moreProjectsGrid) {
        toggleButton.addEventListener('click', () => {
            projectsVisible = !projectsVisible;
            moreProjectsGrid.style.display = projectsVisible ? 'grid' : 'none';
            const currentLang = localStorage.getItem('selectedLang') || 'en';
            const icon = toggleButton.querySelector('i').outerHTML;
            const currentTranslations = translations[currentLang] || translations.en;
            if (projectsVisible) {
                toggleButton.innerHTML = icon + ' ' + currentTranslations.btnShowFewerProjects;
            } else {
                toggleButton.innerHTML = icon + ' ' + currentTranslations.btnShowMoreProjects;
            }
        });
    }

     // --- FAQ Accordion Logic ---
    const faqItems = document.querySelectorAll('.faq-item');

    faqItems.forEach(item => {
        const questionButton = item.querySelector('.faq-question');
        const answerPanel = item.querySelector('.faq-answer');

        if (questionButton && answerPanel) {
            questionButton.addEventListener('click', () => {
                const isExpanded = questionButton.getAttribute('aria-expanded') === 'true';

                // Close all other open FAQ items (optional, for one-at-a-time behavior)
                faqItems.forEach(otherItem => {
                    if (otherItem !== item) {
                        otherItem.classList.remove('open');
                        otherItem.querySelector('.faq-question').setAttribute('aria-expanded', 'false');
                        otherItem.querySelector('.faq-answer').style.maxHeight = null;
                        otherItem.querySelector('.faq-answer').style.paddingTop = '0';
                        otherItem.querySelector('.faq-answer').style.paddingBottom = '0';
                  
                    }
                });


                item.classList.toggle('open');
                questionButton.setAttribute('aria-expanded', !isExpanded);
                
                if (!isExpanded) { // If opening
                    answerPanel.style.display = 'block'; // Must be block to calculate scrollHeight
                    answerPanel.style.maxHeight = answerPanel.scrollHeight + 50 + "px";
                    // Add padding with slight delay for transition to look better
                    setTimeout(() => {
                       if(item.classList.contains('open')) { // check if still open
                           answerPanel.style.paddingTop = '20px';
                           answerPanel.style.paddingBottom = '20px';
                       }
                    }, 50); // Small delay
                } else { // If closing
                    answerPanel.style.maxHeight = null;
                    answerPanel.style.paddingTop = '0';
                    answerPanel.style.paddingBottom = '0';
                    // Hide after transition (optional, depends on if you want it removed from layout)
                    setTimeout(() => {
                        if(!item.classList.contains('open')) { // check if still closed
                            answerPanel.style.display = 'none';
                        }
                    }, 400); // Match transition duration
                }
            });
        }
    });
    // --- End FAQ Accordion Logic ---
});
